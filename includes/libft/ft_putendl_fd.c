#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	unsigned int i;

	i = 0;
	if (s != NULL)
	{
		while (s[i])
		{
			ft_putchar_fd(s[i], fd);
			i++;
		}
		ft_putchar_fd('\n', fd);
	}
}
