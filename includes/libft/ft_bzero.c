#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*tab;
	size_t			i;

	i = 0;
	tab = (unsigned char*)s;
	while (i < n)
	{
		tab[i] = 0;
		i++;
	}
}
